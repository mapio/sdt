#!/bin/bash

./clean.sh

gcc -o toy toy.c
gcc -DNDEBUG -o toy-noassert toy.c
javac Toy.java


echo "C enabled ----------------------------------------------"
./toy
echo -e "--------------------------------------------------------\n"

echo "C disabled ---------------------------------------------"
./toy-noassert
echo -e "--------------------------------------------------------\n"

echo "Java enabled -------------------------------------------"
java -cp . -ea Toy
echo -e "--------------------------------------------------------\n"

echo "Java disabled ------------------------------------------"
java -cp . Toy
echo -e "--------------------------------------------------------\n"

echo "Python enabled -----------------------------------------"
python toy.py
echo -e "--------------------------------------------------------\n"

echo "Python disabled ----------------------------------------"
python -O toy.py
echo -e "--------------------------------------------------------\n"
